package HundredQuestions;

public class ComputeQuotientAndReminder {

	public static void main(String[] args) {

		int dividend = 30;
		int divisor = 2;
		// modulus gives reminder value

		int reminder = dividend % divisor;
		System.out.println("The reminder is::"+reminder);

		// "/" this operator gives quotient value
		
		int Quotinet = dividend / divisor;
		System.out.println("The Quotinet is::"+Quotinet);

	}

}
